#define OLED_W 128
#define OLED_H 64

static void show_logo(const struct device *disp)
{
    #define OLED_W 128
    #define OLED_H 64
    #define IMG_W  128
    #define IMG_H  21

    static uint8_t pages[OLED_W * (OLED_H / 8)];
    memset(pages, 0, sizeof(pages));

    const unsigned char *data = (const unsigned char *)header_data;
    uint8_t rgb[3];
    const int THRESH = 128;

    /* Center offsets for placing the (rotated) image */
    const int off_x = (OLED_W - IMG_W) / 2;       /* = 0 for 128→128 */
    const int off_y = (OLED_H - IMG_H) / 2;       /* = 21 for 64→21 */

    /* Decode source (0..IMG_W-1, 0..IMG_H-1), map to rotated-dst:
       dst = rotate180( off + src ) = (W-1-(off_x+x), H-1-(off_y+y)) */
    for (int y = 0; y < IMG_H; ++y) {
        for (int x = 0; x < IMG_W; ++x) {
            HEADER_PIXEL(data, rgb);
            int luma = (77 * rgb[0] + 150 * rgb[1] + 29 * rgb[2]) >> 8;
            bool on = (luma >= THRESH);

            int dx = OLED_W - 1 - (off_x + x);
            int dy = OLED_H - 1 - (off_y + y);

            uint8_t bit = 1u << (dy & 7);
            pages[(dy >> 3) * OLED_W + dx] |= on ? bit : 0;
        }
    }

    struct display_buffer_descriptor desc = {
        .buf_size = sizeof(pages),
        .width = OLED_W,
        .height = OLED_H,
        .pitch = OLED_W,
        .frame_incomplete = false,
    };

    display_blanking_off(disp);
    display_write(disp, 0, 0, &desc, pages);
}

int main(void)
{
    const struct device *disp = DEVICE_DT_GET(DT_CHOSEN(zephyr_display));
    if (!device_is_ready(disp)) {
        printk("display not ready\n");
        return 0;
    }

    /* Optional: sanity check resolution (will still work if different, but this is the target) */
    struct display_capabilities caps;
    display_get_capabilities(disp, &caps);

    show_logo(disp);

    /* keep it on screen */
    while (1) {
        k_sleep(K_FOREVER);
    }
    return 0;
}

